char	*ft_strupcase(char *str)
{
	int	offset;
	int	i;

	i = 0;
	offset = 'a' - 'A';
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + offset;
		i++;
	}
	return (str);
}
